def node_name(category: dict[str, Any], stem: str, suffix: str) -> str:
    return f"{stem}{category['prefix']}{suffix}"


def remaining_routes(spec: dict[str, Any], index: int) -> list[str]:
    suffix = spec["flow_suffix"]
    routes = [
        node_name(category, "Run", suffix)
        for category in spec["categories"][index + 1 :]
    ]
    routes.append(spec["return_node"])
    return routes


