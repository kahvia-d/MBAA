def generate_shop(spec: dict[str, Any]) -> OrderedDict[str, Any]:
    out: OrderedDict[str, Any] = OrderedDict()
    categories = spec["categories"]
    out[spec["entry"]] = {
        "timeout": 8000,
        "rate_limit": 250,
        "next": ["[JumpBack]NavOpenShop", "[JumpBack]NavLoading", "WaitShopOpened"],
        "on_error": ["ReturnShopToMain"],
    }
    out["WaitShopOpened"] = {
        "recognition": "TemplateMatch",
        "template": "shop/general_tab.png",
        "threshold": 0.7,
        "timeout": 1500,
        "next": ["RunShopPurchasePlan"],
        "roi": [0, 90, 300, 150],
    }
    out["RunShopPurchasePlan"] = {
        "next": [f"Run{category['prefix']}ShopPurchase" for category in categories]
        + ["ReturnShopToMain"]
    }

    for index, category in enumerate(categories):
        prefix = category["prefix"]
        next_routes = [f"Run{c['prefix']}ShopPurchase" for c in categories[index + 1 :]]
        next_routes.append("ReturnShopToMain")
        run = f"Run{prefix}ShopPurchase"
        first_item = f"{prefix}ShopPage1Item1"
        open_spec = category["open"]
        if open_spec["type"] == "coordinate":
            open_node = f"Open{prefix}ShopCategory"
            out[run] = {
                "enabled": False,
                "timeout": 3000,
                "next": [open_node],
                "on_error": next_routes,
            }
            out[open_node] = {
                "action": "Click",
                "target": open_spec["target"],
                "post_wait_freezes": 500,
                "next": [first_item],
                "timeout": 1500,
            }
        else:
            scroll = f"ScrollShopCategoryListTo{prefix}"
            open_node = f"Open{prefix}ShopCategory"
            fallback = f"Open{prefix}ShopCategoryByCoordinate"
            out[run] = {
                "enabled": False,
                "timeout": 3000,
                "next": [scroll],
                "on_error": next_routes,
            }
            out[scroll] = {
                "action": "Swipe",
                "begin": [120, 560],
                "end": [120, 185],
                "duration": 500,
                "post_wait_freezes": 500,
                "next": [open_node],
            }
            out[open_node] = {
                "recognition": "TemplateMatch",
                "template": open_spec["template"],
                "roi": open_spec["roi"],
                "threshold": 0.7,
                "action": "Click",
                "post_wait_freezes": 500,
                "next": [first_item],
                "on_error": [fallback],
                "timeout": 1500,
            }
            out[fallback] = {
                "action": "Click",
                "target": open_spec["fallback_target"],
                "post_wait_freezes": 500,
                "next": [first_item],
                "timeout": 1500,
            }

        for page in range(1, (category["rows"] + 1) // 2 + 1):
            add_shop_page(out, spec, category, index, page)

    out["ReturnShopToMain"] = {"next": ["NavReturnToMain"]}
    return out


