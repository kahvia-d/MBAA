def sweep_stage_option(spec: dict[str, Any], category: dict[str, Any]) -> dict[str, Any]:
    target = node_name(category, "Find", f"{spec['flow_suffix']}StageNumber")
    return {
        "type": "select",
        "default_case": str(spec["default_stage"]),
        "cases": [
            {
                "name": str(stage),
                "pipeline_override": {target: {"expected": f"^{stage:02d}$"}},
            }
            for stage in range(1, spec["stage_count"] + 1)
        ],
    }


def sweep_count_option(spec: dict[str, Any], category: dict[str, Any]) -> dict[str, Any]:
    run = node_name(category, "Run", spec["flow_suffix"])
    add = node_name(category, "Add", "SweepCount")
    cases = []
    for count in range(7):
        add_override: dict[str, Any] = {"enabled": count >= 2}
        if count >= 2:
            add_override["repeat"] = count - 1
        cases.append(
            {
                "name": str(count),
                "label": f"{count} 次",
                "pipeline_override": {
                    run: {"enabled": count > 0},
                    add: add_override,
                },
            }
        )
    return {"type": "select", "default_case": "0", "cases": cases}


def shop_items_option(category: dict[str, Any]) -> dict[str, Any]:
    prefix = category["prefix"]
    cases = []
    for row in range(1, category["rows"] + 1):
        page = (row - 1) // 2 + 1
        for column in range(1, category["columns"] + 1):
            cases.append(
                {
                    "name": f"{row}-{column}",
                    "pipeline_override": {
                        f"Run{prefix}ShopPurchase": {"enabled": True},
                        f"Click{prefix}ShopItemR{row}C{column}": {"enabled": True},
                        f"Purchase{prefix}ShopPage{page}": {"enabled": True},
                    },
                }
            )
    return {"type": "checkbox", "default_case": [], "cases": cases}


def generate_interface(spec: dict[str, Any]) -> OrderedDict[str, Any]:
    base = spec["interface"]
    out: OrderedDict[str, Any] = OrderedDict()
    for key in (
        "interface_version",
        "name",
        "description",
        "contact",
        "license",
        "welcome",
        "github",
        "version",
        "controller",
        "resource",
    ):
        out[key] = base[key]

    tasks = []
    for sweep in spec["sweeps"]:
        options = []
        for category in sweep["categories"]:
            options.extend([f"{category['label']}关号", f"{category['label']}扫荡次数"])
        tasks.append({"name": sweep["task_name"], "entry": sweep["entry"], "option": options})
    tasks.append({"name": spec["shop"]["task_name"], "entry": spec["shop"]["entry"], "option": ["商店购买栏目"]})
    tasks.extend(base["static_tasks"])
    out["task"] = tasks

    options: OrderedDict[str, Any] = OrderedDict()
    cafe = base["cafe_option"]
    options["咖啡厅功能"] = {
        "type": cafe["type"],
        "default_case": cafe["default_case"],
        "cases": [
            {
                "name": case["name"],
                "pipeline_override": {
                    "RunCafe1HeadPat": {"enabled": case["headpat"]},
                    "RunCafeIncomeClaim": {"enabled": case["income"]},
                    "RunCafe2HeadPatPlan": {"enabled": case["cafe2"]},
                },
            }
            for case in cafe["cases"]
        ],
    }
    for sweep in spec["sweeps"]:
        for category in sweep["categories"]:
            options[f"{category['label']}关号"] = sweep_stage_option(sweep, category)
            options[f"{category['label']}扫荡次数"] = sweep_count_option(sweep, category)
    options["商店购买栏目"] = {
        "type": "checkbox",
        "default_case": [],
        "cases": [
            {"name": category["label"], "option": [category["option_name"]]}
            for category in spec["shop"]["categories"]
        ],
    }
    for category in spec["shop"]["categories"]:
        options[category["option_name"]] = shop_items_option(category)
    out["option"] = options
    return out


def render(value: Any) -> str:
    return GENERATED_HEADER + json.dumps(value, ensure_ascii=False, indent=2) + "\n"


def generated_files(spec: dict[str, Any]) -> dict[Path, str]:
    files = {
        ROOT / sweep["output"]: render(generate_sweep(sweep))
        for sweep in spec["sweeps"]
    }
    files[ROOT / spec["shop"]["output"]] = render(generate_shop(spec["shop"]))
    files[ROOT / spec["schedule"]["output"]] = render(generate_schedule(spec["schedule"]))
    files[ROOT / "assets" / "interface.json"] = render(generate_interface(spec))
    return files


def main() -> int:
    parser = argparse.ArgumentParser()
    mode = parser.add_mutually_exclusive_group(required=True)
    mode.add_argument("--write", action="store_true", help="update generated resources")
    mode.add_argument("--check", action="store_true", help="fail when generated resources are stale")
    args = parser.parse_args()

    spec = json.loads(SPEC_PATH.read_text(encoding="utf-8"))
    files = generated_files(spec)
    stale = []
    for path, content in files.items():
        current = path.read_text(encoding="utf-8") if path.exists() else None
        if current == content:
            continue
        if args.write:
            path.parent.mkdir(parents=True, exist_ok=True)
            path.write_text(content, encoding="utf-8", newline="\n")
            print(f"updated {path.relative_to(ROOT)}")
        else:
            stale.append(path.relative_to(ROOT))

    if stale:
        print("Generated resources are stale:", file=sys.stderr)
        for path in stale:
            print(f"  {path}", file=sys.stderr)
        print("Run: python tools/generate_resources.py --write", file=sys.stderr)
        return 1
    if not args.write:
        print("Generated resources are up to date.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
